# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.
# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз
# Подсказка: воспользоваться методом .format()

print('Дано: "яблоко", "банан", "киви", "арбуз"')
aaa = ["яблоко", "банан", "киви", "арбуз"]
ddd = int(0)
for i in aaa:
    ddd += 1
    print('{}.{:>15}'.format(ddd, i))

