# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

kkk = [2, 35, 20, 95, 64, 1980, 22, 88, 200, 3, 20, 35, 12, 4, 1988, 22, 99, 2010]
print("Исходный список ", kkk)
nnn = ['']
for z in kkk:
    vvv = z % 2
    if vvv == 0:
        g = z / 4
        nnn.append(g)
    else:
        a = z * 2
        nnn.append(a)

nnn.pop(0)
print("новый список ", nnn)